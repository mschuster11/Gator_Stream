#ifndef _QUEUE_H_
#define _QUEUE_H_
#include <stdbool.h>
#include <stdint.h>
/* -~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~- */
/* Structs                                                                       */
/* -~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~- */

typedef struct node {
  char uartString[100];
  struct node* next;
  uint16_t length;
  bool isComplete;
  bool isConsumed;
}node;
typedef struct queue {
  node* _head;
  node* _tail;
}queue;
/* -~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~- */
/* Function Prototypes                                                           */
/* -~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~- */
queue* queue_InitQueue(void);
void queue_push(queue* q, node* n);
void queue_pop(queue* q);
node* queue_head(queue* q);
node* queue_tail(queue* q);
#endif
